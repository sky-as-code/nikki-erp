package domain

import (
	dmodel "github.com/sky-as-code/nikki-erp/common/dynamicmodel/model"
	"github.com/sky-as-code/nikki-erp/common/model"
	"github.com/sky-as-code/nikki-erp/modules/core/dynamicmodel/basemodel"
)

const (
	OrganizationalUnitSchemaName = "identity.org_unit"

	OrgUnitFieldId          = basemodel.FieldId
	OrgUnitFieldName        = "name"
	OrgUnitFieldDescription = "description"
	OrgUnitFieldPath        = "path"
	OrgUnitFieldParentId    = "parent_id"
	OrgUnitFieldOrgId       = "org_id"
	OrgUnitFieldUsers       = "users"

	OrgUnitEdgeChildren     = "children"
	OrgUnitEdgeParent       = "parent"
	OrgUnitEdgeEntitlements = "entitlements"
	OrgUnitEdgeOrg          = "org"
)

func OrganizationalUnitSchemaBuilder() *dmodel.ModelSchemaBuilder {
	return dmodel.DefineModel(OrganizationalUnitSchemaName).
		Label(model.LangJson{"en-US": "Organizational Unit"}).
		TableName("ident_org_units").
		CompositeUnique(OrgUnitFieldName, OrgUnitFieldOrgId).
		ShouldBuildDb().
		Extend(basemodel.BaseModelSchemaBuilder()).
		Field(
			dmodel.DefineField().
				Name(OrgUnitFieldName).
				Label(model.LangJson{"en-US": "Name"}).
				DataType(dmodel.FieldDataTypeString(1, model.MODEL_RULE_LONG_NAME_LENGTH)).
				RequiredForCreate().
				Unique(),
		).
		Field(
			dmodel.DefineField().
				Name(OrgUnitFieldDescription).
				Label(model.LangJson{"en-US": "Description"}).
				DataType(dmodel.FieldDataTypeString(0, model.MODEL_RULE_DESC_LENGTH)),
		).
		Field(
			dmodel.DefineField().
				Name(OrgUnitFieldPath).
				Label(model.LangJson{"en-US": "Path"}).
				DataType(dmodel.FieldDataTypeUlid().ArrayType()).
				Description(model.LangJson{"en-US": "Path from org Id, to root and parent orgunit level IDs, and finally to this level ID (inclusive)"}).
				RequiredForCreate().
				AutoGenerated(),
		).
		Field(
			dmodel.DefineField().
				Name(OrgUnitFieldParentId).
				DataType(dmodel.FieldDataTypeUlid()),
		).
		Extend(basemodel.VersionedModelSchemaBuilder()).
		Extend(basemodel.AuditableModelSchemaBuilder()).
		Field(
			dmodel.DefineField().
				Name(OrgUnitFieldOrgId).
				DataType(dmodel.FieldDataTypeUlid()).
				RequiredForCreate(),
		).
		EdgeTo(
			dmodel.Edge(OrgUnitEdgeParent).
				Label(model.LangJson{"en-US": "Parent unit"}).
				ManyToOne(OrganizationalUnitSchemaName, dmodel.DynamicFields{
					OrgUnitFieldParentId: OrgUnitFieldId,
				}).
				OnDelete(dmodel.RelationCascadeCascade),
		).
		EdgeTo(
			dmodel.Edge(OrgUnitEdgeOrg).
				Label(model.LangJson{"en-US": "Organization"}).
				ManyToOne(OrganizationSchemaName, dmodel.DynamicFields{
					OrgUnitFieldOrgId: OrgFieldId,
				}).
				OnDelete(dmodel.RelationCascadeCascade),
		).
		EdgeFrom(
			dmodel.Edge(OrgUnitEdgeChildren).
				Label(model.LangJson{"en-US": "Children units"}).
				Existing(OrganizationalUnitSchemaName, OrgUnitEdgeParent),
		).
		EdgeFrom(
			dmodel.Edge(OrgUnitEdgeEntitlements).
				Label(model.LangJson{"en-US": "Entitlements"}).
				Existing(EntitlementSchemaName, EntitlementEdgeOrgUnit),
		).
		EdgeFrom(
			dmodel.Edge(OrgUnitFieldUsers).
				Label(model.LangJson{"en-US": "Users"}).
				Existing(UserSchemaName, UserEdgeOrgUnit),
		)
}

type OrganizationalUnit struct {
	fields dmodel.DynamicFields
}

func NewOrganizationalUnit() *OrganizationalUnit {
	return &OrganizationalUnit{fields: make(dmodel.DynamicFields)}
}

func NewOrganizationalUnitFrom(src dmodel.DynamicFields) *OrganizationalUnit {
	return &OrganizationalUnit{fields: src}
}

func (this OrganizationalUnit) GetFieldData() dmodel.DynamicFields {
	return this.fields
}

func (this *OrganizationalUnit) SetFieldData(data dmodel.DynamicFields) {
	this.fields = data
}

func (this OrganizationalUnit) GetId() *model.Id {
	return this.fields.GetModelId(basemodel.FieldId)
}

func (this *OrganizationalUnit) SetId(v *model.Id) {
	this.fields.SetModelId(basemodel.FieldId, v)
}

func (this OrganizationalUnit) GetOrgId() *model.Id {
	return this.fields.GetModelId(OrgUnitFieldOrgId)
}

func (this *OrganizationalUnit) SetOrgId(v *model.Id) {
	this.fields.SetModelId(OrgUnitFieldOrgId, v)
}

func (this OrganizationalUnit) GetParentId() *model.Id {
	return this.fields.GetModelId(OrgUnitFieldParentId)
}

func (this *OrganizationalUnit) SetParentId(v *model.Id) {
	this.fields.SetModelId(OrgUnitFieldParentId, v)
}

func (this OrganizationalUnit) GetPath() []string {
	return this.fields.GetStrings(OrgUnitFieldPath)
}

func (this *OrganizationalUnit) SetPath(path []string) {
	this.fields.SetStrings(OrgUnitFieldPath, path)
}
