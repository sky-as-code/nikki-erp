func (this *LoginAttempt) NextMethod() *string {
	allMethods := this.MustGetMethods()
	curMethod := this.GetCurrentMethod()
	if curMethod == nil {
		if len(allMethods) == 0 {
			return nil
		}
		return util.ToPtr(allMethods[0])
	}
	total := len(allMethods)
	for i := 0; i < total-1; i++ {
		if allMethods[i] == *curMethod {
			return util.ToPtr(allMethods[i+1])
		}
	}
	return nil
}
