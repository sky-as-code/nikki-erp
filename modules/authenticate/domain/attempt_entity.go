func (this *LoginAttempt) NextMethod() *string {
	if this.GetCurrentMethod() == nil {
		m := this.GetMethods()
		if len(m) == 0 {
			return nil
		}
		return util.ToPtr(m[0])
	}
	total := len(this.GetMethods())
	cur := this.GetCurrentMethod()
	for i := 0; i < total-1; i++ {
		if this.GetMethods()[i] == *cur {
			return util.ToPtr(this.GetMethods()[i+1])
		}
	}
	return nil
}

func (this *LoginAttempt) SetDefaults() {
	id, err := model.NewId()
	if err != nil {
		panic(err)
	}
	this.SetId(id)
	this.SetIsGenuine(util.ToPtr(false))
	this.SetStatus(util.ToPtr(AttemptStatusPending))
}
