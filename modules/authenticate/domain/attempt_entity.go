type AttemptStatus string

const (
	AttemptStatusSuccess = AttemptStatus("success")
	AttemptStatusFailed  = AttemptStatus("failed")
	AttemptStatusPending = AttemptStatus("pending")
)

const (
	LoginAttemptSchemaName = "authenticate.login_attempt"

	AttemptFieldMethods        = "methods"
	AttemptFieldCurrentMethod  = "current_method"
	AttemptFieldDeviceIp       = "device_ip"
	AttemptFieldDeviceName     = "device_name"
	AttemptFieldDeviceLocation = "device_location"
	AttemptFieldExpiresAt      = "expires_at"
	// The principal trying to login. Can be Nikki user or a custom user (e.g: customer account).
	AttemptFieldPrincipalType = "principal_type"
	AttemptFieldStatus        = "status"
	// Username is specific to the subject. For Nikki user, it is the email.
	AttemptFieldUsername = "username"
)

func LoginAttemptSchemaBuilder() *dmodel.ModelSchemaBuilder {
	return dmodel.DefineModel(LoginAttemptSchemaName).
		Label(model.LangJson{"en-US": "Login Attempt"}).
		TableName("authn_attempts").
		ShouldBuildDb().
		Extend(basemodel.BaseModelSchemaBuilder()).
		Extend(basemodel.AuditableModelSchemaBuilder()).
		Field(
			dmodel.DefineField().Name(AttemptFieldMethods).
				Label(model.LangJson{"en-US": "Methods"}).
				DataType(dmodel.FieldDataTypeString(1, 64).ArrayType()).
				RequiredForCreate().
				AutoGenerated(),
		).
		Field(
			dmodel.DefineField().Name(AttemptFieldCurrentMethod).
				Label(model.LangJson{"en-US": "Current method"}).
				DataType(dmodel.FieldDataTypeString(1, 64)).
				RequiredForCreate().
				AutoGenerated(),
		).
		Field(
			dmodel.DefineField().Name(AttemptFieldDeviceIp).
				Label(model.LangJson{"en-US": "Device IP"}).
				DataType(dmodel.FieldDataTypeString(0, 45)),
		).
		Field(
			DefinePrincipalDeviceNameField().
				Label(model.LangJson{"en-US": "Device name"}),
		).
		Field(
			dmodel.DefineField().Name(AttemptFieldDeviceLocation).
				Label(model.LangJson{"en-US": "Device location"}).
				DataType(dmodel.FieldDataTypeString(0, model.MODEL_RULE_SHORT_NAME_LENGTH)),
		).
		Field(
			dmodel.DefineField().Name(AttemptFieldExpiresAt).
				Label(model.LangJson{"en-US": "Expires at"}).
				DataType(dmodel.FieldDataTypeDateTime()).
				RequiredForCreate().
				AutoGenerated(),
		).
		Field(
			DefinePrincipalTypeField(AttemptFieldPrincipalType).
				Label(model.LangJson{"en-US": "Principal type"}).
				Default(PrincipalTypeNikkiUser).
				RequiredForCreate(),
		).
		Field(
			dmodel.DefineField().Name(AttemptFieldStatus).
				Label(model.LangJson{"en-US": "Status"}).
				DataType(dmodel.FieldDataTypeEnumString([]string{
					string(AttemptStatusPending), string(AttemptStatusSuccess), string(AttemptStatusFailed),
				})).
				Default(AttemptStatusPending).
				RequiredForCreate(),
		).
		Field(
			DefinePrincipalUsernameField(AttemptFieldUsername).
				Label(model.LangJson{"en-US": "Username"}).
				RequiredForCreate(),
		)
}

